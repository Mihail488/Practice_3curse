// Задача 1 - Среднее арифметическое положительных элементов
function среднееПоложительных() {
    const массив = Array.from({length: 15}, () => Math.floor(Math.random() * 41) - 10);
    const положительные = массив.filter(число => число > 0);
    const среднее = положительные.reduce((сумма, число) => сумма + число, 0) / положительные.length;
    
    return {
        исходныйМассив: массив,
        среднееЗначение: среднее
    };
}

// Задача 2 - Замена отрицательных элементов квадратами
function заменаОтрицательных(массив) {
    return массив.map(число => число < 0 ? число * число : число);
}

// Задача 3 - Статистика массива
function статистикаМассива(массив) {
    const положительные = массив.filter(число => число > 0);
    const отрицательные = массив.filter(число => число < 0);
    const нули = массив.filter(число => число === 0).length;
    
    return {
        среднееПоложительных: положительные.length ? 
            положительные.reduce((сумма, число) => сумма + число, 0) / положительные.length : 0,
        среднееОтрицательных: отрицательные.length ? 
            отрицательные.reduce((сумма, число) => сумма + число, 0) / отрицательные.length : 0,
        количествоНулей: нули
    };
}

// Задача 4 - Симулятор цветов
function симуляторЦветов() {
    const ИТЕРАЦИЙ = 1000000;
    let максКрасный = 0;
    let текущийКрасный = 0;
    
    for (let i = 0; i < ИТЕРАЦИЙ; i++) {
        const цвет = Math.floor(Math.random() * 3); // 0 - красный, 1 - черный, 2 - белый
        
        if (цвет === 0) {
            текущийКрасный++;
            максКрасный = Math.max(максКрасный, текущийКрасный);
        } else {
            текущийКрасный = 0;
        }
    }
    
    return максКрасный;
}